class Product:
    def __init__(self, name, price, more_info):
        self.name = name
        self.price = price
        self.more_info = more_info

    
    def get_price(self):
        return self.price
    
    def get_name(self):
        return self.name
        
    def get_name(self):
        return self.more_info   
